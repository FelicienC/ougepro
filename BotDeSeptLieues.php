 private function randArray($arr)
 { return $arr[mt_rand(0, count($arr) - 1)]; }

 private function creerPluriel($nom)
 {
  if ($nom[2] !== null) return $nom[2];

  if ($this->endsWith($nom[0], "s")) return $nom[0];
  if ($this->endsWith($nom[0], "x")) return $nom[0];
  if ($this->endsWith($nom[0], "z")) return $nom[0];
  if ($this->endsWith($nom[0], "eau")) return $nom[0]."x";

  return $nom[0]."s";
 }

 private function endsWith($haystack, $needle)
 {
  $length = strlen($needle);
  if ($length == 0) return true;
  return (substr($haystack, -$length) === $needle);
 }

