 {
  $jour = ceil(time() / (24 * 3600));
  // mt_srand($jour);
  //$jour -= 17366;
  $jour -= 17368;

  $texte = "Jour ".strval($jour)."\n\n";

  $struct = $this->randArray($this->structures);
  $texte .= $this->randArray($struct[0]);
  $texte .= " ".$this->randArray($struct[1]);

  switch ($struct[2])
  {
   case "lieu":
    $sujet = $this->randArray($this->lieux);
    break;
   default:
    $sujet = $this->randArray($this->noms);
    break;
  }

  $texte = str_replace("%adj%", $this->creerAdjectif(), $texte);

  if ($sujet[1] == 1) // sujet féminin
   $texte = str_replace("€", "e", $texte);
  else
   $texte = str_replace("€", "", $texte);

  $texte = str_replace("%comp0%", $this->creerComplement(0), $texte);
  $texte = str_replace("%comp1%", $this->creerComplement(1), $texte);
  $texte = str_replace("%comp2%", $this->creerComplement(2), $texte);
  $texte = str_replace("%comp3%", $this->creerComplement(3), $texte);

  $texte = str_replace("%un_sujet%", $this->creerGroupeNominal($sujet, "un", false), $texte);

  $texte = str_replace("%nom%", $this->creerGroupeNominal($this->randArray($this->noms), "", false), $texte);
