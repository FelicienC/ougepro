 private function creerGroupeNominal($nom, $article, $pluriel)
 {
  if ($pluriel) $gn = $this->creerPluriel($nom);
  else $gn = $nom[0];

  if (($article === 0) || (strlen($article) == 0)) return $gn;

 if ($nom[1] == 1) // féminin
 {
  switch ($article)
  {
   case "un": $article = "une"; break;
   case "le": $article = "la"; break;
  }
 }

  $gn = $article." ".$gn;
 
  return $gn;
 }

 private function creerComplement($index)
 {
  $compArr = $this->randArray($this->complements);
  $comp = str_replace('*', $compArr[$index + 1], $compArr[0]);

  if (mb_strpos($comp, '+') !== FALSE)
  {
   if (($index == 1) || ($index == 3)) // pluriel
    $comp = str_replace('+', $compArr[6], $comp);
   else
    $comp = str_replace('+', $compArr[5], $comp);
  }

  return $comp;
 }

 private function creerAdjectif()
 {
  $adj = $this->randArray($this->adjectifs);

  if (mt_rand(0, 1) == 0)
   $adj = $this->randArray($this->adverbes)." ".$adj;

  return $adj;
 }

